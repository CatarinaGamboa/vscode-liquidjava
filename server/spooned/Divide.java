

public class Divide {
    public static int divide(int numerator, int denominator) {
        return numerator / denominator;
    }

    public static void main(java.lang.String[] args) {
        int a;
        a = Divide.divide(10, 5);
        a = Divide.divide(50, 10);
        a = Divide.divide(800, 0);
    }
}

